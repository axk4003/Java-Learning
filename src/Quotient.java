public class Quotient {
    public static void main(String[] args) {
        int num1 = 20;
        int num2 = 3;
        int quotient = num1 / num2;
        int remainder = num1 % num2;
        System.out.println("the quotient is:" + quotient);
        System.out.println("the remainder is:" + remainder);
    }
}


